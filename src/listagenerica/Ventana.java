    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        tablaP.setRowCount(0);
        int pos = tabla.getSelectedRow();
        TListaGenerica lis = listaP.nodoPosicion(pos).getLista();
        TNodo tem = lis.getCabeza();
        while(tem != null){
            tablaP.setRowCount(tablaP.getRowCount()+1);
            mostrar(tablaP.getRowCount()-1, (TProducto)tem.getDatos());
            tem = tem.getSiguiente();
        }
    }//GEN-LAST:event_jButton10ActionPerformed

