    private void limpiar(){
        cedula.setText("");
        nombres.setText("");
        apellidos.setText("");
        telefono.setText("");
        cedula.grabFocus();
    }
    
    private void mostrar(int fila, TPersona persona){
        tablaM.setValueAt(persona.getCedula(), fila, 0);
        tablaM.setValueAt(persona.getApellidos(), fila, 1);
        tablaM.setValueAt(persona.getNombres(), fila, 2);
        tablaM.setValueAt(persona.getTelefono(), fila, 3);
        tablaM.setValueAt(persona.getCorreo(), fila, 4);
    }
    
    private void mostrar(int fila, TProducto producto){
        tablaP.setValueAt(producto.getNombre(), fila, 0);
        tablaP.setValueAt(producto.getCantidad(), fila, 1);
        tablaP.setValueAt(producto.getPrecio(), fila, 2);
        tablaP.setValueAt(producto.getFechaCompra(), fila, 3);
    }
    
    private void mostrar(){
        int fila = 0;
        TNodoP<TPersona> nodo = listaP.getCabeza();
        while(nodo != null){
            tablaM.setRowCount(fila++);
            mostrar(fila,nodo.getDatos());
            fila++;
            nodo = nodo.getSiguiente();
        }
        
    }
    
    private void llenar(TPersona persona){
        persona.setCedula(Integer.parseInt(cedula.getText()));
        persona.setApellidos(apellidos.getText());
        persona.setNombres(nombres.getText());
        persona.setTelefono(Integer.parseInt(telefono.getText()));
        persona.setCorreo(correo.getText());
    }
    
    private void llenar(TProducto producto){
        producto.setNombre(nombreP.getText());
        producto.setCantidad(Integer.parseInt(cantidad.getText()));
        producto.setFechaCompra(fecha.getText());
        producto.setPrecio(Integer.parseInt(precio.getText()));
    }
    
    
    

